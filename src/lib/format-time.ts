// The user's operations are US-based, so every displayed timestamp uses US
// Eastern time (America/New_York), which correctly switches between
// EDT/EST across daylight saving -- never the browser's local timezone.
export function formatEdt(date: Date | string | null | undefined): string {
  if (!date) return "—";
  const d = typeof date === "string" ? new Date(date) : date;
  return new Intl.DateTimeFormat("en-US", {
    timeZone: "America/New_York",
    year: "numeric",
    month: "short",
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit",
    second: "2-digit",
    hour12: true,
    timeZoneName: "short",
  }).format(d);
}
